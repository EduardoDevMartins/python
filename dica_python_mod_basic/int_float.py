# Tipos de int e float
# int -> Número Inteiro 
# O tipo int representa qualquer número
# positivo ou negativo, int sem sinal é considerado positivo.

print(11)  # int
print(-11)  # int
print(0) #numero literal int


# float -> Número com ponto flutuante 
#O tipo float representa qualquer numero, positivo ou negativo.
#float sem sinal é considerado positivo

print(1.1) #sempre usar ponto para separar casas decimais
print(0.0 , -1.5) #float


# a função type motra o tipo que o pyton inferiu ao valor.
print(type('otavio'))