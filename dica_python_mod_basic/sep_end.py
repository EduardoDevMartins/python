# \r\n não esta na tela mas é usado para realizar as quebras de linha

# sep=' ', é usado para criar um novo tipo de separador
print(12, 24, 48,  'adasdad', sep='---',)
# end=' ', é usado para definir o tipo de quebra de linha
print(12, 24, 48, 'adasdad', end='.....')
# end='\n ', colocar a qquebra de linha que definir antes da proxima linha 
print(12, 24, 48, 'adasdad', end='\n......')
print(12, 24, 48, 'adasdad')
