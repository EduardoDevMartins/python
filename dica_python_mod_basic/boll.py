# Tipo de dado boll (bolean)
# Ao questionar algo em um programa,
# Só existem duas respostas possiveis:
# Sim (true) Não (false).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual ao outro

print(10==10) #sim = > True (verdadeiro)
print(10==9) #não = > False (falso)
print(10>8)
print(type(10==10)) #class = bool
print(type(False))